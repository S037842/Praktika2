namespace OL_PRAKTIKA
{
    public class Group
    {
        private int id;
        private string Name;

        public void setId(int _id)
        {
            id = _id;
        }

        public void setName(string _name)
        {
            Name = _name;
        }

        public int getId()
        {
            return id;
        }

        public string getName()
        {
            return Name;
        }
    }
}
